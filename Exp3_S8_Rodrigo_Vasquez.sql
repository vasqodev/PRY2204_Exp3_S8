-- 1. CREACIÓN DE TABLAS FUERTES (Sin dependencias)
--    AJUSTE 1: IDENTITY con INCREMENT BY correcto.
--    AJUSTE 2: Constraints CHECK y UNIQUE se agregan
--              después con ALTER TABLE.
--------------------------------------------------------

-- PAIS: IDENTITY inicia en 9, incrementa en 3 (9, 12, 15, ...)
CREATE TABLE PAIS (
    id_pais NUMBER(3) GENERATED BY DEFAULT AS IDENTITY (START WITH 9 INCREMENT BY 3) NOT NULL,
    nom_pais VARCHAR2(30) NOT NULL,
    CONSTRAINT PK_PAIS PRIMARY KEY(id_pais)
);

-- CLIENTE: Se crea SIN el UNIQUE de email y SIN el CHECK de dv.
-- Se agregarán después con ALTER TABLE (punto 2).
CREATE TABLE CLIENTE (
    rut NUMBER(8) NOT NULL,
    dv CHAR(1) NOT NULL,
    pnombre VARCHAR2(20) NOT NULL,
    snombre VARCHAR2(20),
    apaterno VARCHAR2(20) NOT NULL,
    amaterno VARCHAR2(20) NOT NULL,
    telefono VARCHAR2(12),
    email VARCHAR2(40),
    tipo_cli CHAR(1) NOT NULL,
    CONSTRAINT PK_CLIENTE PRIMARY KEY(rut)
);

CREATE TABLE MARCA (
    id_marca NUMBER(2) NOT NULL,
    descripcion VARCHAR2(20) NOT NULL,
    CONSTRAINT PK_MARCA PRIMARY KEY(id_marca)
);

-- SERVICIO: Sin IDENTITY. Se usará una SECUENCIA para poblar (punto 3).
CREATE TABLE SERVICIO (
    id_servicio NUMBER(3) NOT NULL,
    descripcion VARCHAR2(100) NOT NULL,
    costo NUMBER(7) NOT NULL,
    CONSTRAINT PK_SERVICIO PRIMARY KEY(id_servicio)
);

CREATE TABLE TIPO_AUTOMOVIL (
    id_tipo CHAR(3) NOT NULL,
    descripcion VARCHAR2(20) NOT NULL,
    CONSTRAINT PK_TIPO_AUTOMOVIL PRIMARY KEY(id_tipo)
);

-- MECANICO: IDENTITY inicia en 460, incrementa en 7 (460, 467, 474, ...).
-- Se crea SIN el CHECK de sueldo; se agrega después con ALTER TABLE (punto 2).
CREATE TABLE MECANICO (
    cod_mecanico NUMBER(5) GENERATED BY DEFAULT AS IDENTITY (START WITH 460 INCREMENT BY 7) NOT NULL,
    pnombre VARCHAR2(20) NOT NULL,
    snombre VARCHAR2(20) NOT NULL,
    apaterno VARCHAR2(20) NOT NULL,
    amaterno VARCHAR2(20) NOT NULL,
    bono_jefatura NUMBER(10),
    sueldo NUMBER(10) NOT NULL,
    monto_impuestos NUMBER(10) NOT NULL,
    cod_supervisor NUMBER(5),
    CONSTRAINT PK_MECANICO PRIMARY KEY(cod_mecanico),
    CONSTRAINT FK_MECANICO_MECANICO FOREIGN KEY(cod_supervisor) REFERENCES MECANICO(cod_mecanico),
    CONSTRAINT CK_MECANICO_BONO_JEFATURA CHECK(bono_jefatura >= 0),
    CONSTRAINT CK_MECANICO_MONTO_IMPUESTOS CHECK(monto_impuestos >= 0)
);


