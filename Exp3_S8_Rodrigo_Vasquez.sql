--------------------------------------------------------
-- Simular un ajuste salarial del 5% a los mecánicos que:
--   - Tienen un sueldo BETWEEN 600000 AND 900000, o
--   - Su cod_supervisor IS NULL.
-- Columnas: IDENTIFICADOR, MECANICO, SALARIO ACTUAL, AJUSTE, SUELDO REAJUSTADO
-- Orden: sueldo ASC; en caso de empate, nombre completo DESC.

SELECT
    cod_mecanico                                    AS "IDENTIFICADOR",
    pnombre || ' ' || snombre || ' ' || apaterno    AS "MECANICO",
    sueldo                                          AS "SALARIO ACTUAL",
    sueldo * 0.05                                   AS "AJUSTE",
    sueldo * 1.05                                   AS "SUELDO REAJUSTADO"
FROM MECANICO
WHERE sueldo BETWEEN 600000 AND 900000
   OR cod_supervisor IS NULL
ORDER BY sueldo ASC, (pnombre || ' ' || snombre || ' ' || apaterno) DESC;
